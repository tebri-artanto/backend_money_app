const mongoose = require('mongoose');

const BulanSchema = new mongoose.Schema({
    bln: {
        type: String,
        // required: true,
    },
    tahun: {
        type: String,
        // required: true,
    },
    pemasukan: {
        type: Number,
        // required: true,
    },
    pengeluaran: {
        type: Number,
        // required: true,
    },
    total: {
        type: Number,
        // required: true,
    },
    riwayat: [
        { type: mongoose.Schema.Types.ObjectId, ref: 'Riwayat' } // Referencing the activities model
    ]

});

const Bulan = mongoose.model("Bulan", BulanSchema);


module.exports = Bulan;

























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































